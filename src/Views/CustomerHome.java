    public void loadCustomerDetails()
    {
        DBManager db = new DBManager();
        Customer loggedInCustomer = new Customer();
        loggedInCustomer = db.getCustomerFromUsername(loggedInUser.getUsername());
        
        String username = loggedInCustomer.getUsername();
        String firstName = loggedInCustomer.getFirstName();
        String lastName = loggedInCustomer.getLastName();
        Date dateOfBirth = loggedInCustomer.getDateOfBirth();
        String email = loggedInCustomer.getEmail();
        String house = loggedInCustomer.getHouse();
        String street = loggedInCustomer.getStreet();
        String town = loggedInCustomer.getTown();
        String postcode = loggedInCustomer.getPostcode();
        String telephone = loggedInCustomer.getTelephone();
        String mobile = loggedInCustomer.getMobile();
        
        txtUsername.setText(username);
        txtUsername.setEnabled(false);
        txtFirstName.setText(firstName);
        txtLastName.setText(lastName);
        jdateDateOfBirth.setDate(dateOfBirth);
        txtEmail.setText(email);
        txtHouse.setText(house);
        txtStreet.setText(street);
        txtTown.setText(town);
        txtPostcode.setText(postcode);
        txtTelephone.setText(telephone);
        txtMobile.setText(mobile);
    }
    
