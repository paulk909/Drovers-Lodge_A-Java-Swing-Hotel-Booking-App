    public void loadStaffTable()
    {
        tblStaff.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);        
        DefaultTableModel model = (DefaultTableModel)tblStaff.getModel();
        HashMap<Integer, Staff> staff = new HashMap<Integer, Staff>();
        DBManager db = new DBManager();
        staff = db.getStaff();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        for(Map.Entry<Integer, Staff> staffEntry : 
                staff.entrySet())
        {
            Staff currentStaff = staffEntry.getValue();      
            String s = db.getStaffRoleFromStaffRoleID(currentStaff.getStaffRoleID());
            int ss = currentStaff.getStaffRoleID();      
            model.addRow(new Object[]{ currentStaff.getStaffID(), currentStaff.getUsername(), currentStaff.getFirstName(),
            currentStaff.getLastName(), currentStaff.getEmail(), db.getStaffRoleFromStaffRoleID(currentStaff.getStaffRoleID()) });
        }
    }
    
    public void clearStaffTable()
    {
        DefaultTableModel model = (DefaultTableModel)tblStaff.getModel(); 
        int rows = model.getRowCount(); 
        for(int i = rows - 1; i >=0; i--)
        {
           model.removeRow(i); 
        }
    }
    
    public void populateStaffRoleDropDown()
    {
        comboStaffRole.removeAllItems();
        DBManager db = new DBManager();        
        HashMap<Integer, StaffRole> staffRoles = new HashMap<Integer, StaffRole>();
        staffRoles = db.getStaffRoles(); 
        comboStaffRole.addItem("--Please Select--");
        for (Map.Entry<Integer, StaffRole> staffRoleEntry : staffRoles.entrySet())
        {
            comboStaffRole.addItem(staffRoleEntry.getValue().getStaffRole());
        }
    }

