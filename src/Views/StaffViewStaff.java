    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed
        Staff editableStaff = new Staff();
        int staffID = (Integer)tblStaff.getValueAt(tblStaff.getSelectedRow(), 0);
        DBManager db = new DBManager();
        editableStaff = db.getStaffFromStaffID(staffID);
                
        String username = editableStaff.getUsername();
        String firstName = editableStaff.getFirstName();
        String lastName = editableStaff.getLastName();
        String email = editableStaff.getEmail();
        int staffRoleID = editableStaff.getStaffRoleID();
        
        this.populateStaffRoleDropDown();
        
        txtStaffID.setText(String.valueOf(staffID));
        txtStaffID.setEnabled(false);
        txtUsername.setText(username);
        txtUsername.setEnabled(false);
        txtFirstName.setText(firstName);
        txtLastName.setText(lastName);
        txtEmail.setText(email);
        comboStaffRole.setSelectedIndex(staffRoleID);
    }//GEN-LAST:event_btnClearActionPerformed

